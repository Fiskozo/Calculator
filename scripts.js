const add = function(x, y) {
    let result = x + y;
    return result;	
  };
  
const subtract = function(x, y) {
    let result = x - y;
    return result;		
  };

const multiply = function(x, Y) {
    let result = x * Y;
    return result;
};

const divide = function(x, Y) {
    let result = x / Y;
    return result;
};

